function APICall( call, args, completion )
{
    if( typeof networkAdmin != "undefined" ){ args += '&networkAdmin=' + encodeURIComponent(networkAdmin); }

    var xhrCall;
    if( window.XMLHttpRequest ){ xhrCall = new XMLHttpRequest(); }else{ xhrCall = new ActiveXObject("Microsoft.XMLHTTP"); }
    xhrCall.open( "POST", call, true );
    xhrCall.setRequestHeader("Content-Type", "application/x-www-form-urlencoded");
    xhrCall.onreadystatechange = function()
    {
        if( xhrCall.readyState == 4 )
        {
            if( completion != undefined )
            {
                completion( xhrCall );
            }
        }
    }
                       
    xhrCall.send( args );
}
